
package comprimentocircunferência;


public class ComprimentoCircunferência {

    
    public static void main(String[] args) {
        
        double raio = 3;
        
        double diametro = 2*raio;
        
        double pi = 3.14;
        
        double C = pi*diametro;
        
        System.out.println("O comprimento de uma circunferencia é " +C);
    }
    
}
